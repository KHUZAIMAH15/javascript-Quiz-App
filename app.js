      let restartBtn = document.getElementById("restartBtn");

      let totalMarks = 0;
      let timer = questions[0].time;
      let selectedAnswer = null;
      let quizEnded = false;
    
      let checkAnswer = (optionIndex, btn) => {
        if (quizEnded) return;
        let currentQuestion = questions[questionIndex];
        selectedAnswer = currentQuestion.options[optionIndex];

      
        let buttons = displayoptions.getElementsByTagName("button");
        for (let i = 0; i < buttons.length; i++) {
          buttons[i].classList.remove("bg-blue-800", "text-white");
          buttons[i].classList.add("text-gray-800");
        }
        
       
        btn.classList.add("bg-blue-800", "text-white");
        btn.classList.remove("text-gray-800");
      };

      let renderQuestion = () => {
        let currentQuestion = questions[questionIndex];

        currentQuestionNo.innerHTML = questionIndex + 1;
        totalQuestionNo.innerHTML = questions.length;
        displayQuestion.innerHTML = currentQuestion.question;

        displayoptions.innerHTML = "";
        selectedAnswer = null;

        for (let i = 0; i < currentQuestion.options.length; i++) {
          let option = currentQuestion.options[i];
          displayoptions.innerHTML += `     
           <button onclick="checkAnswer(${i}, this)" 
              class="flex items-center p-3 text-gray-800 border rounded-lg cursor-pointer hover:bg-blue-700 hover:text-white transition-colors">
              ${option}
            </button>`;
        }

        let progress = ((questionIndex + 1) / questions.length) * 100;
        progressBar.style.width = progress + "%";

        timer = currentQuestion.time;
        timerDisplay.innerText = timer;
      };

      const checkTimer = () => {
        if (timer > 0) {
          timer--;
          timerDisplay.innerHTML = timer;
        } else {
          nextQuestion();
        }
      };

      let nextQuestion = () => {
        let currentQuestion = questions[questionIndex];

      
        if (selectedAnswer && selectedAnswer === currentQuestion.correctAnswer) {
          totalMarks += currentQuestion.mark;
        }

   
        if (questionIndex + 1 === questions.length) {
          endQuiz();
        } else {
          questionIndex++;
          renderQuestion();
        }
      };

